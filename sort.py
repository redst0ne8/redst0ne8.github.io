def merge_sort(arr):
    if len(arr) > 1:
        # Step 1: Divide - Find the midpoint and split the array
        mid = len(arr) // 2
        left_half = arr[:mid]
        right_half = arr[mid:]

        # Step 2: Conquer - Recursively sort both halves
        merge_sort(left_half)
        merge_sort(right_half)

        # Step 3: Combine - Merge the sorted halves back into the original array
        i = j = k = 0

        # Compare elements from left_half and right_half
        while i < len(left_half) and j < len(right_half):
            if left_half[i] < right_half[j]:
                arr[k] = left_half[i]
                i += 1
            else:
                arr[k] = right_half[j]
                j += 1
            k += 1

        # Collect any remaining elements from left_half
        while i < len(left_half):
            arr[k] = left_half[i]
            i += 1
            k += 1

        # Collect any remaining elements from right_half
        while j < len(right_half):
            arr[k] = right_half[j]
            j += 1
            k += 1

